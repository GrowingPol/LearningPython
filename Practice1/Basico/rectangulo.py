alto = float(input("Introduzca el alto del rectángulo en metros: "))
ancho = float(input("Introduzca el ancho del rectángulo en metros: "))
area = alto * ancho
perimeter = (alto*2 + ancho*2)

print(f"El área del Rectángulo es de {area} metros")
print(f"El perímetro del Rectángulo es de {perimeter} metros")
